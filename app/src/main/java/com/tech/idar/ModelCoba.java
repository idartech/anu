package com.tech.idar;

public class ModelCoba {
    String nim, nama;

    public ModelCoba() {
    }

    public ModelCoba(String nim, String nama) {
        this.nim = nim;
        this.nama = nama;
    }

    public String getNim() {
        return nim;
    }

    public String getNama() {
        return nama;
    }
}
